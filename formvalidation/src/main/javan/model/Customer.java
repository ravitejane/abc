package model;

import java.io.Serializable;
import java.util.Date;

import javax.validation.constraints.Max;
import javax.validation.constraints.Min;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Past;
import javax.validation.constraints.Size;

import org.hibernate.validator.constraints.Email;
import org.hibernate.validator.constraints.NotEmpty;
import org.springframework.format.annotation.DateTimeFormat;

import validator.Phone;

public class Customer implements Serializable
{
	
	private static final long serialVersionUID = -1638742219410078712L;

	@Size(min=2,max=30)
	private String name;
	
	@NotEmpty @Email
	private String email;
	
	@NotNull @Min(18) @Max(150)
	private Integer age;
	
	@NotNull
	private Gender gender;
	
	@Past @NotNull
	@DateTimeFormat(pattern="MM/DD/YYYY")
	private Date birthday;
	
	@Phone
	private String phoneno;
	
	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return name+" "+email+" "+age+" "+birthday+" "+gender;
	}
	
	public enum Gender {
			MALE,FEMALE
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public Date getDateOfBirth() {
		return birthday;
	}
	public void setDateOfBirth(Date dateOfBirth) {
		this.birthday = dateOfBirth;
	}
	public Gender getGender() {
		return gender;
	}
	public void setGender(Gender gender) {
		this.gender = gender;
	}
	public String getPhoneno() {
		return phoneno;
	}
	public void setPhoneno(String phoneno) {
		this.phoneno = phoneno;
	}
	public Customer(String name, String email, Integer age, Date dateOfBirth, Gender gender, String phoneno) {
		super();
		this.name = name;
		this.email = email;
		this.age = age;
		this.birthday = dateOfBirth;
		this.gender = gender;
		this.phoneno = phoneno;
	}
	public Customer() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((age == null) ? 0 : age.hashCode());
		result = prime * result + ((birthday == null) ? 0 : birthday.hashCode());
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + ((gender == null) ? 0 : gender.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((phoneno == null) ? 0 : phoneno.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Customer other = (Customer) obj;
		if (age == null) {
			if (other.age != null)
				return false;
		} else if (!age.equals(other.age))
			return false;
		if (birthday == null) {
			if (other.birthday != null)
				return false;
		} else if (!birthday.equals(other.birthday))
			return false;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (gender != other.gender)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (phoneno == null) {
			if (other.phoneno != null)
				return false;
		} else if (!phoneno.equals(other.phoneno))
			return false;
		return true;
	}

}
